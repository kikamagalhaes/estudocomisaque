package aula01;

public class IfElse {

	public static void main(String[] args) {

		char sexo = 'M';
		
		if(sexo == 'M'){
			System.out.println("seu sexo é masculino");
			
		} else if (sexo == 'F'){
			System.out.println("seu sexo é feminino");
			
		}	else {
			System.out.println("valor não reconhecido");
			
		}
		
	}

}
