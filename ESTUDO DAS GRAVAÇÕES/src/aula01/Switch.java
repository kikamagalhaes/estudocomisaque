package aula01;

public class Switch {

	public static void main(String[] args) {
	
		char sexo = 'M';
		
		switch(sexo) {
		
			case 'M' :
				System.out.println("Seu sexo é masculino");
				break;
				
			case 'F' :
				System.out.println("Seu sexo é feminino");
				break;
				
			default :
				System.out.println("Valor não reconhecido");
				
				
				
			
		}
			
			
		
	}

}
